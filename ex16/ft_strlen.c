#include <stdio.h>

int	ft_strlen(char *str)
{
	int	length;

	length = 0;
	while (*str)
	{
		length++;
		str++;
	}
	return (length);
}

int	main(void)
{
	char	string[25] = "teste";

	printf("%d\n", ft_strlen(string));
}
